package com.nhnacademy;

public class MatchHistory {
    
    int matches = 0;
    int wins = 0;

    public void setMatches(int matches) {
        this.matches = matches;
    }

    public void setWins(int wins) {
        this.wins = wins;
    }

    public int getMatches() {
        return matches;
    }

    public int getWins() {
        return wins;
    }
}
